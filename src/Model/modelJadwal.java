package Model;

public class modelJadwal {
    private String id, hari_ke, penceramah_id, kode_waktu, tema;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHari_ke() {
        return hari_ke;
    }

    public void setHari_ke(String hari_ke) {
        this.hari_ke = hari_ke;
    }

    public String getPenceramah_id() {
        return penceramah_id;
    }

    public void setPenceramah_id(String penceramah_id) {
        this.penceramah_id = penceramah_id;
    }

    public String getKode_waktu() {
        return kode_waktu;
    }

    public void setKode_waktu(String kode_waktu) {
        this.kode_waktu = kode_waktu;
    }

    public String getTema() {
        return tema;
    }

    public void setTema(String tema) {
        this.tema = tema;
    }
}
