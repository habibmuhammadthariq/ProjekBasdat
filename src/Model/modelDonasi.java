package Model;

public class modelDonasi {
    private String id, donatur_id, hari, bentuk;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDonatur_id() {
        return donatur_id;
    }

    public void setDonatur_id(String donatur_id) {
        this.donatur_id = donatur_id;
    }

    public String getHari() {
        return hari;
    }

    public void setHari(String hari) {
        this.hari = hari;
    }

    public String getBentuk() {
        return bentuk;
    }

    public void setBentuk(String bentuk) {
        this.bentuk = bentuk;
    }
    
}
